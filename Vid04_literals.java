package com.tushar;
/*
 * A constant value that is assigned to the variable is called Literals.
 */
public class Vid04_literals {
    public static void main(String[] args) {
        byte age = 65;
        int age2 = 45;
        short age3 = 78;
        long ageOfWhale = 667765456;
        char ch = 'a';
        float f1 = 65.87f;
        double d1 = 67.876d; //or 67.876 (since it is default)
        boolean bool = true;
        String str = "tushar";
        System.out.println(age+"\n"+age2+"\n"+age3+"\n"+ageOfWhale+"\n"+ch+"\n"+f1+"\n"+d1+"\n"+bool+"\n"+str);
    }
}
